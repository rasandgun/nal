#ifndef SEMANTIC_H
#define SEMANTIC_H

#include "ast.h"
#include <map>

/**
 * @brief Класс, занимающийся семантическим анализом
 * @details
 * Содержит методы, позволяющие проводить семантический анализ. 
 * В процессе анализа поддерживает области видимости, проверяет совместимости типов,
 * проверяет находится ли continue или break внутри цикла, наличие return у non-void функции,
 * и т. д.
 * 
 * @section workflow Процесс анализа
 * 1. Сбор всех объявлений функций и глобальных переменных
 * 2. Обход AST с проверкой семантических правил
 * 3. Построение таблицы символов с учётом областей видимости
 * 
 * @section checks Выполняемые проверки
 * - Объявление переменных перед использованием
 * - Совместимость типов в операциях и присваиваниях
 * - Корректность вызовов функций (количество и типы аргументов)
 * - Наличие return в не-void функциях
 * - Правильность использования break/continue (только внутри циклов)
 * - Типы условий в if, while, for (должны быть скалярными)
 * 
 * @code
 * SemanticAnalyzer sem;
 * Program* ast = parser.parse();
 * sem.analyze(ast);  // Бросает исключение при ошибке
 * @endcode
 * 
 * @date 2026-04-02
 */
class SemanticAnalyzer {
public:
    /**
     * @brief Конструктор семантического анализатора
     * @details Инициализирует глобальную область видимости и начальные параметры
     */
    SemanticAnalyzer();
    
    /**
     * @brief Деструктор, освобождающий память таблицы символов
     */
    ~SemanticAnalyzer();
    
    /**
     * @brief Запуск семантического анализа для всей программы
     * @param program Корневой узел AST программы
     * @throw std::runtime_error При обнаружении семантической ошибки
     * 
     * @note В случае ошибки анализ прекращается, выбрасывается исключение
     */
    void analyze(Program* program);
    
private:
    /**
     * @brief Структура, описывающая символ в таблице символов
     * 
     * Содержит информацию об идентификаторе:
     * - тип (для переменных и функций)
     * - флаг, является ли символ функцией
     * - параметры (только для функций)
     */
    struct Symbol {
        Type type;                      ///< Тип символа
        bool isFunction;                ///< true - функция, false - переменная
        std::vector<Parameter> params;  ///< Параметры (только для функций)
    };
    
    /**
     * @brief Класс, представляющий область видимости
     * @details
     * Область видимости — это контекст, в котором определены имена.
     * Области образуют иерархию (вложенные блоки).
     * 
     * @section example Пример использования
     * @code
     * Scope* global = new Scope();
     * global->declare("x", Type(TYPE_INT));
     * Scope* local = global->pushScope();
     * local->declare("y", Type(TYPE_FLOAT));
     * // В local видны и x, и y
     * local->popScope();  // Возвращает global
     * @endcode
     * 
     * @note Поиск символов идёт от текущей области вверх к глобальной
     */
    class Scope {
    public:
        /**
         * @brief Конструктор области видимости
         * @param parent Родительская область (nullptr для глобальной)
         */
        Scope(Scope* parent = nullptr);
        
        /**
         * @brief Деструктор области видимости
         * @details Удаляет только текущую область (не родительскую)
         */
        ~Scope();
        
        /**
         * @brief Объявление нового символа в текущей области
         * @param name Имя символа
         * @param type Тип символа
         * @param isFunc Флаг функции (по умолчанию false)
         * @param params Параметры (только для функций)
         * @throw std::runtime_error Если символ уже объявлен в текущей области
         */
        void declare(const std::string& name, const Type& type, 
                     bool isFunc = false, 
                     const std::vector<Parameter>& params = std::vector<Parameter>());
        
        /**
         * @brief Отметить символ как определённый (инициализированный)
         * @param name Имя символа
         * @throw std::runtime_error Если символ не найден
         * @note Пока не используется для проверки инициализации
         */
        void define(const std::string& name);
        
        /**
         * @brief Поиск символа в текущей и родительских областях
         * @param name Имя символа
         * @return Указатель на символ или NULL, если не найден
         */
        Symbol* lookup(const std::string& name);
        
        /**
         * @brief Получение типа символа
         * @param name Имя символа
         * @return Указатель на тип или NULL, если символ не найден
         */
        Type* getType(const std::string& name);
        
        /**
         * @brief Проверка, является ли символ функцией
         * @param name Имя символа
         * @return true - функция, false - не функция или не найден
         */
        bool isFunction(const std::string& name);
        
        /**
         * @brief Получение параметров функции
         * @param name Имя функции
         * @return Указатель на вектор параметров или NULL
         */
        std::vector<Parameter>* getParams(const std::string& name);
        
        /**
         * @brief Создание новой вложенной области видимости
         * @return Указатель на новую область (текущая становится родительской)
         */
        Scope* pushScope();
        
        /**
         * @brief Выход из текущей области видимости
         * @return Указатель на родительскую область
         * @note Текущая область уничтожается
         */
        Scope* popScope();
        
    private:
        std::map<std::string, Symbol> symbols;  ///< Таблица символов в текущей области
        Scope* parent;                          ///< Родительская область (NULL для глобальной)
    };

    Scope* global;              ///< Глобальная область видимости
    Scope* current;             ///< Текущая область видимости
    Type currentReturnType;     ///< Тип возвращаемого значения текущей анализируемой функции
    bool inLoop;                ///< Флаг, указывающий, находимся ли мы внутри цикла
    bool foundReturn;           ///< Флаг, указывающий, найден ли return в текущем блоке
    /**
     * @brief Сбор всех объявлений функций и глобальных переменных
     * @param prog Корневой узел программы
     * @details
     * Первый проход по AST: регистрирует все функции и глобальные переменные
     * в глобальной области видимости. Это позволяет вызывать функции до их определения.
     */
    void collectDeclarations(Program* prog);
    
    /**
     * @brief Рекурсивный обход AST с проверкой семантики
     * @param node Текущий узел AST
     * @throw std::runtime_error При обнаружении семантической ошибки
     * 
     * @details
     * Для каждого типа узла выполняются специфические проверки:
     * - Блоки создают новые области видимости
     * - Выражения проверяются на совместимость типов
     * - Циклы управляют флагом inLoop
     * - Функции проверяют типы параметров и возвращаемого значения
     */
    void analyzeNode(ASTNode* node);
    
    /**
     * @brief Вычисление и проверка типа выражения
     * @param node Узел выражения
     * @return Тип выражения
     * @throw std::runtime_error При ошибке типизации
     * 
     * @details
     * Рекурсивно вычисляет тип выражения и проверяет семантику:
     * - Литералы имеют фиксированные типы
     * - Идентификаторы должны быть объявлены
     * - Операторы должны иметь операнды совместимых типов
     * - Вызовы функций проверяются на соответствие сигнатуре
     * - Доступ к массивам требует целочисленного индекса
     */
    Type getExprType(Expression* node);
    
    /**
     * @brief Проверка совместимости двух типов
     * @param left Левый тип
     * @param right Правый тип
     * @return true - типы совместимы, false - несовместимы
     * 
     * @details
     * Правила совместимости:
     * - Типы должны быть одинаковыми
     * - Допускается преобразование int ↔ float
     * - Допускается преобразование int ↔ char
     * - Допускается преобразование int ↔ bool
     * - Массивы совместимы только при полном совпадении
     */
    bool typeCompatible(const Type& left, const Type& right);
    
    /**
     * @brief Проверка, является ли тип скалярным
     * @param t Проверяемый тип
     * @return true - скалярный тип, false - не скалярный
     * @details
     * Скалярные типы: все типы, кроме void и массивов.
     * Используется для проверки условий в if, while, for.
     */
    bool isScalar(const Type& t);
    
    /**
     * @brief Проверка, является ли тип арифметическим
     * @param t Проверяемый тип
     * @return true - арифметический тип, false - не арифметический
     * @details
     * Арифметические типы: int, float, char (не массивы, не bool, не void)
     * Используется для арифметических операций (+, -, *, /, %).
     */
    bool isArithmetic(const Type& t);
    
    /**
     * @brief Проверка, является ли тип целочисленным
     * @param t Проверяемый тип
     * @return true - целочисленный тип, false - не целочисленный
     * @details
     * Целочисленные типы: int, char, bool (не float, не массивы, не void)
     * Используется для индексов массивов.
     */
    bool isInteger(const Type& t);
    void declareBuiltins();
};

#endif